package day2;

public class Variabletask {

	public static void main(String[] args) {
		byte a = 22;
		short b = 32000;
		int c = 2000000;
		long d = 7643773877l;
		double e = 23.45565567;
		float f = 23.45f;
		boolean g = true;
		      
		System.err.println("byte value "+  a);
		System.err.println("Short value " + b);
		System.err.println("int value " + c);
		System.err.println("long value "+d);
		System.err.println("double value "+e);
		System.err.println("float value "+f);
		System.err.println("Boolean value "+g);
	}

}
